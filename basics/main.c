#include <stdio.h>

int main()
{
    printf("Hello, World!\n");

    // Variables
    int i = 10, j = 20;
    char c = 'H';
    float f = 10.5;
    double d = 100.44446;

    printf("i = %d, j = %d\n", i, j);
    printf("c = %c\n", c);
    printf("f = %f\n", f);
    printf("d = %lf\n", d);

    return 0;
}